uint16_t get_alt_repeat_key_keycode_user(uint16_t keycode, uint8_t mods) {
    if (osm(keycode))
    {
        set_oneshot_mods(MOD_BIT(KC_RSFT));
        return KC_NO;
    }
    if (get_highest_layer(layer_state) == _WHIRL) {
        switch (keycode) {
            case KC_W: return KC_H;
            case KC_Y: return KC_COMM;
            case KC_U: return KC_E;
            case KC_G: return KC_S;
            case KC_QUOT: return KC_M;
        }
    }
    if (get_highest_layer(layer_state) == _CHOG) {
        switch (keycode) {
            case KC_R: return KC_L;
            case KC_Y: return KC_COMM;
            case KC_D: return KC_S;
            case KC_G: return KC_S;
            case KC_C: return KC_T;
            case KC_O: return KC_U;
            case QK_AREP: return KC_W;
            case KC_T: return KC_DOT;
        }
    }
    return keycode;  // Defer to default definitions.
}
bool osm(uint16_t keycode) {
    if (last_input_activity_elapsed() > 300)
        return true;
    switch (keycode) {
        case KC_SPC: case LCTL(KC_BSPC): case KC_MINS: case KC_UNDS: case KC_ESC: case KC_ENT: case KC_TAB: return true;
    }
    return false;
}

